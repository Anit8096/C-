#include<iostream>
using namespace std;
int main(){
    int a = 1, b = 0;
    cout <<"Value of a && b is " << (a && b) << endl;            // AND operator
    cout <<"value of a || b is " << (a || b) << endl;            // OR operator
    cout <<"Value of !a is " << (!a) << endl;                    // NOT operator
}